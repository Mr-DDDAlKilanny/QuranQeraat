    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        SelectionDetail d = (SelectionDetail) jList1.getSelectedValue();
        if (d == null) return;
        QeraatGroupJPanel j = new QeraatGroupJPanel();
        j.set(d);
        if (JOptionPane.showConfirmDialog(getParent(),
                        j,
                        "تحرير بيانات القراءة",
                        JOptionPane.OK_CANCEL_OPTION,
                        JOptionPane.PLAIN_MESSAGE) == JOptionPane.OK_OPTION) {
            j.get();
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        SelectionDetail d = (SelectionDetail) jList1.getSelectedValue();
        if (d == null) return;
        if (JOptionPane.showConfirmDialog(this, "حذف الخلاف المحدد؟")
                == JOptionPane.OK_OPTION) {
            ((DefaultListModel) jList1.getModel()).removeElement(d);
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        QeraatGroupJPanel j = new QeraatGroupJPanel();
        SelectionDetail d = new SelectionDetail();
        d.type = SelectionType.Farsh;
        d.descr = "";
        d.shatibiyyah = new ArrayList<>();
        d.dorrah = new ArrayList<>();
        d.rewayaat = new RewayahSelectionList();
        j.set(d);
        if (JOptionPane.showConfirmDialog(getParent(),
                        j,
                        "تحرير بيانات القراءة",
                        JOptionPane.OK_CANCEL_OPTION,
                        JOptionPane.PLAIN_MESSAGE) == JOptionPane.OK_OPTION) {
            d = j.get();
            ((DefaultListModel) jList1.getModel()).addElement(d);
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed


