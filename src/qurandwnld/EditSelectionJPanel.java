    public void set(Selection s) {
        DefaultListModel model = (DefaultListModel) jList1.getModel();
        model.clear();
        if (s.details == null)
            s.details = new ArrayList<>();
        for (SelectionDetail d : s.details)
            model.addElement(d);
        jTextField1.setText(s.ayahWord);
        Point p = DbHelper.getAyah(s.ayahIndex);
        jComboBoxSurah.setSelectedIndex(p.x - 1);
        jSpinnerAyah.setValue(p.y);
        selection = s;
    }
    
    
    void editSelectionDetail(SelectionDetail s) {
        InputQeraatJDialog j = new InputQeraatJDialog((Frame) this.getParent(), true);
        j.set(s);
        JOptionPane.showMessageDialog(this, j.getContentPane(), "تحرير بيانات القراءة",
                JOptionPane.PLAIN_MESSAGE);
        j.get();
        //DbHelper.updateSelectionDetail(s);
    }
    
    
    public Selection get() {
        selection.ayahIndex = DbHelper.getAyahIndex(jComboBoxSurah.getSelectedIndex() + 1, 
                (int) jSpinnerAyah.getValue());
        selection.ayahWord = jTextField1.getText();
        selection.details.clear();
        for (int i = 0; i < jList1.getModel().getSize(); ++i) {
            selection.details.add((SelectionDetail) jList1.getModel().getElementAt(i));
        }
        return selection;
    }

